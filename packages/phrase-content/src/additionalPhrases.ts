function combineSeeds(
  tier: PhraseTier,
  prefix: string,
  starts: readonly PhraseEntry[],
  ends: readonly PhraseEntry[],
): readonly PhraseSeed[] {
  return starts.flatMap(([startText, startKana], startIndex) => ends.map(([endText, endKana], endIndex) => ({
    id: `${prefix}_${String(startIndex * ends.length + endIndex + 1).padStart(3, "0")}`,
    displayText: `${startText}${endText}`,
    readingKana: `${startKana}${endKana}`,
    tier,
    category: "追加語彙",
  })));
}

