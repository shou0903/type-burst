import type { PhraseSeed, PhraseTier } from "./types";

type PhraseEntry = readonly [displayText: string, readingKana: string];

function createSeeds(
  tier: PhraseTier,
  prefix: string,
  entries: readonly PhraseEntry[],
): readonly PhraseSeed[] {
  return entries.map(([displayText, readingKana], index) => ({
    id: `${prefix}_${String(index + 1).padStart(3, "0")}`,
    displayText,
    readingKana,
    tier,
    category: "追加語彙",
  }));
}

